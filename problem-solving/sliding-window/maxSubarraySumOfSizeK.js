function maxSubarraySumOfSizeK(nums, k) {
  //  —Ä–∞—Ö—É—î–º–æ –≤—Å—ñ –µ–ª–µ–º–µ–Ω—Ç–∏ –∑ –º–∞—Å–∏–≤—É
  let currentSum = 0;
  //  —Ä–∞—Ö—É—î–º–æ —Ä–æ–∑–º—ñ—Ä –≤—ñ–∫–Ω–∞
  let windowSize = 0;
  //  –Ω–∞—à —Ä–µ–∑—É–ª—å—Ç–∞—Ç
  let maxSum = -Infinity;
  //  –¥—ñ–≤–∞ —á–∞—Å—Ç–∏–Ω–∞ –≤—ñ–∫–Ω–∞
  let left = 0;

  for (let right = 0; right < nums.length; right++) {
    // —Å—É–º—É—î–º–æ –∫–æ–∂–µ–Ω –µ–ª–µ–º–µ–Ω—Ç –∑ –º–∞—Å–∏–≤—É
    currentSum += nums[right];

    // —Ä–∞—Ö—É—î–º–æ —Ä–æ–∑–º—ñ—Ä –≤—ñ–∫–Ω–∞.
    windowSize = right - left + 1;

    // —è–∫—â–æ —Ä–æ–∑–º—ñ—Ä –≤—ñ–∫–Ω–∞ –¥–æ—Ä—ñ–≤–Ω—é—î —Ç–∞—Ä–≥–µ—Ç—É - –∞–±–æ –∫–æ–ª–∏ –º–∏ –∑–∞–ø–æ–≤–Ω–∏–ª–∏ —Ä–æ–∑–º—ñ—Ä –≤—ñ–∫–Ω–∞ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ—é
    // –∫—ñ–ª—å–∫–æ—Å—Ç—Ç—é –µ–ª–µ–º–µ–Ω—Ç—ñ–≤ —Ç–æ–¥—ñ –≤–∂–µ –ø–æ—á–∏–Ω–∞–∂–º–æ –∫–æ–≤–∑–∞—Ç–∏ –ø–æ –º–∞—Å–∏–≤—É —Å–∞–º–µ –≤—ñ–∫–Ω–æ–º
    if (windowSize === k) {
      // –æ–Ω–æ–≤–ª—é—î–º–æ –º–∞–∫—Å–µ–º–∞–ª—å–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è
      maxSum = Math.max(maxSum, currentSum);
      // —Ä–æ–±–∏–º–æ –º—ñ–Ω—É—Å –∑ –∑–∞–≥–∞–ª—å–Ω–æ—ó —Å—É–º—É –∞ —Ç–æ—á–Ω—ñ—à–µ –≤—ñ–¥–Ω—ñ–º–∞—î–º–æ –Ω–µ–ø–æ—Ç—Ä—ñ–±–Ω–µ
      currentSum -= nums[left];
      // —Ä—É—Ö–∞—î–º–æ –ª—ñ–≤—É —á–∞—Å—Ç–∏–Ω—É –≤–ø–µ—Ä–µ–¥
      left++;
    }
  }

  return maxSum;
}

